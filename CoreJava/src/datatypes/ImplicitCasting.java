package datatypes;

public class ImplicitCasting {

	public static void main(String[] args) {
		char c=43;  //implicit casting
		System.out.println(c);  //prints 'd'
		
		int i='a'; //implicit casting
		System.out.println(i);   //prints 97
		
		double d='a';
		System.out.println(d);  //prints 97.0
		
		//char x=97.0;
		

	}

}
