package datatypes;

public class StringConcatenation {

	public static void main(String[] args) {
		String s="Sachin";
		System.out.println(s);
		
		System.out.println(s.concat(" Tendulkar"));
		
		System.out.println(s+"Tendulkar");
        System.out.println(s+" "+"Tendulkar");
	}

}
